import { spawnSync } from "node:child_process";
import { mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve } from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";
import { compile } from "json-schema-to-typescript";
import type { JSONSchema } from "json-schema-to-typescript";

type Version = "v16" | "v201" | "v21";
type MessageKind = "Request" | "Response";
type JsonObject = Record<string, unknown>;

interface MessageSpec {
  action: string;
  kind: MessageKind;
  schemaImportName: string;
  schemaKey: string;
  sourcePath: string;
  typeFileBase: string;
  typeName: string;
  version: Version;
  versionSuffix: string;
}

const versions: Version[] = ["v16", "v201", "v21"];
const versionSuffixes: Record<Version, string> = {
  v16: "V16",
  v201: "V201",
  v21: "V21",
};
const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const vendorDir = join(rootDir, "vendor", "ocpp-schemas");
const outputDir = join(rootDir, "src", "ocpp");
const generatedHeader =
  "// Generated by scripts/generate-ocpp.ts. Do not edit.\n\n";
const nestedV201Exports = [
  "ReportDataType",
  "VariableAttributeType",
  "VariableCharacteristicsType",
];

function isJsonObject(value: unknown): value is JsonObject {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

async function readJsonObject(path: string): Promise<JsonObject> {
  const parsed: unknown = JSON.parse(await readFile(path, "utf8"));

  if (!isJsonObject(parsed)) {
    throw new Error(`${path} did not contain a JSON object`);
  }

  return parsed;
}

function cloneJsonObject(value: JsonObject): JsonObject {
  const cloned: unknown = JSON.parse(JSON.stringify(value));

  if (!isJsonObject(cloned)) {
    throw new Error("JSON object clone did not produce an object");
  }

  return cloned;
}

function lowerFirst(value: string): string {
  return `${value.charAt(0).toLowerCase()}${value.slice(1)}`;
}

function kebabCase(value: string): string {
  return value
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1-$2")
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .toLowerCase();
}

function modulePath(fromDir: string, toPath: string): string {
  const path = relative(fromDir, toPath).replace(/\\/g, "/");

  return path.startsWith(".") ? path : `./${path}`;
}

function inferKind(fileStem: string): MessageKind {
  return fileStem.endsWith("Response") ? "Response" : "Request";
}

function actionName(fileStem: string, kind: MessageKind): string {
  return fileStem.endsWith(kind) ? fileStem.slice(0, -kind.length) : fileStem;
}

function rootBaseName(fileStem: string, kind: MessageKind): string {
  return fileStem.endsWith(kind) ? fileStem : `${fileStem}${kind}`;
}

async function discoverSpecs(version: Version): Promise<MessageSpec[]> {
  const versionDir = join(vendorDir, version);
  const files = (await readdir(versionDir))
    .filter((file) => file.endsWith(".json"))
    .sort((left, right) => left.localeCompare(right));

  return files
    .map((file) => {
      const fileStem = file.slice(0, -".json".length);
      const kind = inferKind(fileStem);
      const baseName = rootBaseName(fileStem, kind);
      const versionSuffix = versionSuffixes[version];
      const typeName = `${baseName}${versionSuffix}`;
      const schemaKey = lowerFirst(typeName);

      return {
        action: actionName(fileStem, kind),
        kind,
        schemaImportName: `${schemaKey}Schema`,
        schemaKey,
        sourcePath: join(versionDir, file),
        typeFileBase: kebabCase(baseName),
        typeName,
        version,
        versionSuffix,
      };
    })
    .sort((left, right) => left.typeName.localeCompare(right.typeName));
}

async function writeGenerated(path: string, contents: string): Promise<void> {
  await mkdir(dirname(path), { recursive: true });
  await writeFile(path, `${contents.trimEnd()}\n`, "utf8");
}

function stripSchemaComments(contents: string): string {
  return contents
    .replace(/\/\*\*[\s\S]*?\*\/\n?/g, "")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

async function writeTypeFile(spec: MessageSpec): Promise<void> {
  const schema = cloneJsonObject(await readJsonObject(spec.sourcePath));
  schema.title = spec.typeName;

  const compiled = await compile(schema as JSONSchema, spec.typeName, {
    bannerComment: "",
    format: false,
    style: {
      semi: true,
      singleQuote: false,
      tabWidth: 2,
      trailingComma: "all",
    },
    unknownAny: true,
    unreachableDefinitions: false,
  });

  await writeGenerated(
    join(outputDir, spec.version, "types", `${spec.typeFileBase}.ts`),
    `${generatedHeader}${stripSchemaComments(compiled)}`,
  );
}

async function writeSchemasFile(
  version: Version,
  specs: MessageSpec[],
): Promise<void> {
  const versionDir = join(outputDir, version);
  const imports = specs.map((spec) => {
    const source = modulePath(versionDir, spec.sourcePath);

    return `import ${spec.schemaImportName} from "${source}";`;
  });
  const entries = specs.map(
    (spec) => `  ${spec.schemaKey}: ${spec.schemaImportName},`,
  );

  await writeGenerated(
    join(versionDir, "schemas.ts"),
    `${generatedHeader}${imports.join("\n")}

export const ${version}Schemas: Record<string, object> = {
${entries.join("\n")}
};`,
  );
}

async function writeValidatorsFile(
  version: Version,
  specs: MessageSpec[],
): Promise<void> {
  const versionDir = join(outputDir, version);
  const typeImports = specs.map((spec) => {
    return `import type { ${spec.typeName} } from "./types/${spec.typeFileBase}";`;
  });
  const validators = specs.map((spec) => {
    return `export const isValid${spec.typeName} = (
  data: unknown,
): data is ${spec.typeName} =>
  validateOCPPPayload(${version}Schemas.${spec.schemaKey}, data);`;
  });
  const requestSpecs = specs.filter((spec) => spec.kind === "Request");
  const actionEntries = requestSpecs.map((spec) => {
    return `  ${JSON.stringify(spec.action)}: isValid${spec.typeName},`;
  });

  await writeGenerated(
    join(versionDir, "validators.ts"),
    `${generatedHeader}import { validateOCPPPayload } from "../validate";
import { ${version}Schemas } from "./schemas";
${typeImports.join("\n")}

${validators.join("\n\n")}

export const actionValidator${versionSuffixes[version]}: Record<
  string,
  (data: unknown) => boolean
> = {
${actionEntries.join("\n")}
};`,
  );
}

async function writeVersionIndex(
  version: Version,
  specs: MessageSpec[],
): Promise<void> {
  const typeExports = specs.map((spec) => {
    if (version === "v201" && spec.typeName === "NotifyReportRequestV201") {
      return `export type { ${[spec.typeName, ...nestedV201Exports].join(
        ", ",
      )} } from "./types/${spec.typeFileBase}";`;
    }

    return `export type { ${spec.typeName} } from "./types/${spec.typeFileBase}";`;
  });

  await writeGenerated(
    join(outputDir, version, "index.ts"),
    `${generatedHeader}export * from "./schemas";
export * from "./validators";
${typeExports.join("\n")}`,
  );
}

async function writeSchemaAggregate(): Promise<void> {
  await writeGenerated(
    join(outputDir, "schemas.ts"),
    `${generatedHeader}import { v16Schemas } from "./v16/schemas";
import { v201Schemas } from "./v201/schemas";
import { v21Schemas } from "./v21/schemas";

export const schemas: {
  v16: Record<string, object>;
  v201: Record<string, object>;
  v21: Record<string, object>;
} = {
  v16: v16Schemas,
  v201: v201Schemas,
  v21: v21Schemas,
};`,
  );
}

async function writeValidateModule(): Promise<void> {
  await writeGenerated(
    join(outputDir, "validate.ts"),
    `${generatedHeader}import Ajv from "ajv";
import type { AnySchema, AnySchemaObject, ValidateFunction } from "ajv";
import draft06MetaSchema from "ajv/dist/refs/json-schema-draft-06.json";
import Ajv04 from "ajv-draft-04";

interface SchemaCompiler {
  compile(schema: AnySchema): ValidateFunction;
}

const ajvOptions = {
  allErrors: true,
  strict: false,
  strictSchema: false,
  validateFormats: false,
};

const draft04Ajv = new Ajv04(ajvOptions) as SchemaCompiler;
const draft06AjvInstance = new Ajv(ajvOptions);
draft06AjvInstance.addMetaSchema(draft06MetaSchema as AnySchemaObject);
const draft06Ajv = draft06AjvInstance as SchemaCompiler;
const draft04Cache = new WeakMap<object, ValidateFunction>();
const draft06Cache = new WeakMap<object, ValidateFunction>();

function isDraft04Schema(schema: object): boolean {
  const schemaVersion = (schema as { $schema?: unknown }).$schema;

  return typeof schemaVersion === "string" && schemaVersion.includes("draft-04");
}

function validatorCache(schema: object): WeakMap<object, ValidateFunction> {
  return isDraft04Schema(schema) ? draft04Cache : draft06Cache;
}

function validatorCompiler(schema: object): SchemaCompiler {
  return isDraft04Schema(schema) ? draft04Ajv : draft06Ajv;
}

function validatorFor(schema: object): ValidateFunction {
  const cache = validatorCache(schema);
  const cached = cache.get(schema);

  if (cached) {
    return cached;
  }

  const compiled = validatorCompiler(schema).compile(schema as AnySchema);
  cache.set(schema, compiled);

  return compiled;
}

export function validateOCPPPayload(schema: object, data: unknown): boolean {
  if (data == null) {
    return false;
  }

  return validatorFor(schema)(data) === true;
}

export function validationErrors(schema: object, data: unknown): string[] {
  const validate = validatorFor(schema);

  if (validate(data)) {
    return [];
  }

  return (validate.errors ?? []).map((error) =>
    \`\${error.instancePath} \${error.message ?? ""}\`.trim(),
  );
}`,
  );
}

