async function writeCompatibilityModules(): Promise<void> {
  await writeGenerated(
    join(outputDir, "validation", "index.ts"),
    `${generatedHeader}export { validateOCPPPayload, validationErrors } from "../validate";
export * from "./v16";
export * from "./v201";
export * from "./v21";`,
  );

  for (const version of versions) {
    await writeGenerated(
      join(outputDir, "validation", version, "index.ts"),
      `${generatedHeader}export * from "../../${version}/validators";`,
    );
  }

  await writeGenerated(
    join(outputDir, "types", "v201", "notify-report.ts"),
    `${generatedHeader}export type { ${nestedV201Exports.join(
      ", ",
    )} } from "../../v201/types/notify-report-request";`,
  );
}

function formatOutput(): void {
  const prettierBin = join(
    rootDir,
    "node_modules",
    ".bin",
    process.platform === "win32" ? "prettier.cmd" : "prettier",
  );
  const result = spawnSync(
    prettierBin,
    ["--write", outputDir, "--log-level", "silent"],
    {
      cwd: rootDir,
      encoding: "utf8",
    },
  );

  if (result.status !== 0) {
    throw new Error(
      `Prettier failed while formatting generated OCPP files.\n${result.stdout ?? ""}${result.stderr ?? ""}`,
    );
  }
}

async function main(): Promise<void> {
  const specsByVersion = new Map<Version, MessageSpec[]>();

  for (const version of versions) {
    specsByVersion.set(version, await discoverSpecs(version));
  }

  await rm(outputDir, { force: true, recursive: true });
  await mkdir(outputDir, { recursive: true });

  for (const version of versions) {
    const specs = specsByVersion.get(version);

    if (!specs) {
      throw new Error(`No schema specs discovered for ${version}`);
    }

    for (const spec of specs) {
      await writeTypeFile(spec);
    }

    await writeSchemasFile(version, specs);
    await writeValidatorsFile(version, specs);
    await writeVersionIndex(version, specs);
  }

  await writeSchemaAggregate();
  await writeValidateModule();
  await writeEnumsModule();
  await writeRootIndex();
  await writeCompatibilityModules();
  formatOutput();

  const counts = versions.map((version) => {
    const count = specsByVersion.get(version)?.length ?? 0;

    return `${version}: ${count}`;
  });

  console.log(`Generated OCPP code (${counts.join(", ")})`);
}

await main();
